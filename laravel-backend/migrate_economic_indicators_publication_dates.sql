-- 経済指標の公表日情報を投入するSQLスクリプト
-- 現在のVueファイルのgetIndicatorData()メソッドから抽出した公表日情報

-- 公表日情報をmetadataフィールドに保存
UPDATE economic_indicators 
SET metadata = json_build_object(
    'publication_schedule', 
    CASE name
        -- 景気カテゴリー
        WHEN 'GDP速報' THEN '四半期毎の翌々月(中旬)'
        WHEN '景気動向指数(全国)' THEN '翌々月の上旬'
        WHEN '景気動向指数(福岡県)' THEN '翌々月の31日'
        WHEN '日銀短観(全国)' THEN '四半期毎の翌月1日'
        WHEN '日銀短観(九州)' THEN '四半期毎の翌月1日'
        WHEN '景気ウォッチャー調査(全国、九州)' THEN '翌月の6営業日'
        WHEN '福岡県内の経済情勢報告' THEN '四半期毎の翌月25日～31日'
        WHEN '福岡市の地場企業経営動向調査' THEN '四半期毎の翌月OR翌々月'
        WHEN '北九州市の地場企業経営動向調査' THEN '四半期毎の翌月'
        WHEN '久留米市の地場企業景況調査' THEN '四半期毎の翌々々月の上旬'
        
        -- 個人消費カテゴリー
        WHEN '商業販売額速報(全国)' THEN '翌月の下旬(29～31日)'
        WHEN '大型小売店業態別販売額(全国)' THEN '翌月の下旬(29～31日)'
        WHEN '大型小売店業態別販売額(九州、福岡県)' THEN '翌々月の第2営業日～第4営業日'
        WHEN 'コンビニエンスストア・専門量販店販売額(九州)' THEN '翌々月の第2営業日～第4営業日'
        WHEN '消費動向調査(全国)' THEN '翌月の中旬'
        WHEN '消費活動指数' THEN '翌月の第5営業日'
        
        -- 投資カテゴリー
        WHEN '建築着工統計(全国、福岡県)' THEN '翌々月の31日'
        WHEN '公共工事請負金額(福岡県)' THEN '翌々月の31日前後'
        WHEN '機械受注統計(全国)' THEN '翌々月の10日前後'
        WHEN '設備投資計画調査(全国、九州)' THEN '翌年の8月'
        
        -- 貿易カテゴリー
        WHEN '貿易統計(九州、福岡県)' THEN '翌月の下旬'
        
        -- 生産カテゴリー
        WHEN '鉱工業生産指数(全国)' THEN '翌月の31日前後'
        WHEN '鉱工業生産指数(九州)' THEN '翌々月第2週目OR第3週目'
        WHEN '鉱工業生産指数(福岡県)' THEN '翌々月の31日前後'
        
        -- 雇用・所得カテゴリー
        WHEN '完全失業率(全国)' THEN '翌月の31日前後'
        WHEN '有効求人倍率(全国、福岡県)' THEN '翌月の31日前後'
        WHEN '毎月勤労統計調査地方調査(福岡県の所得・雇用実態)' THEN '翌々月の下旬(20日～31日)'
        
        -- 金融カテゴリー
        WHEN '企業倒産状況(全国、福岡県)' THEN '翌々月の下旬(20日～31日)'
        WHEN '日経平均株価' THEN '翌月の6営業日'
        
        -- 海外指標カテゴリー
        WHEN '米ドル対円相場' THEN '翌月の1日'
        WHEN 'ユーロ対円相場' THEN '翌月の1日'
        
        ELSE '未設定'
    END
)
WHERE name IN (
    'GDP速報', '景気動向指数(全国)', '景気動向指数(福岡県)', '日銀短観(全国)', '日銀短観(九州)',
    '景気ウォッチャー調査(全国、九州)', '福岡県内の経済情勢報告', '福岡市の地場企業経営動向調査',
    '北九州市の地場企業経営動向調査', '久留米市の地場企業景況調査', '商業販売額速報(全国)',
    '大型小売店業態別販売額(全国)', '大型小売店業態別販売額(九州、福岡県)',
    'コンビニエンスストア・専門量販店販売額(九州)', '消費動向調査(全国)', '消費活動指数',
    '建築着工統計(全国、福岡県)', '公共工事請負金額(福岡県)', '機械受注統計(全国)',
    '設備投資計画調査(全国、九州)', '貿易統計(九州、福岡県)', '鉱工業生産指数(全国)',
    '鉱工業生産指数(九州)', '鉱工業生産指数(福岡県)', '完全失業率(全国)',
    '有効求人倍率(全国、福岡県)', '毎月勤労統計調査地方調査(福岡県の所得・雇用実態)',
    '企業倒産状況(全国、福岡県)', '日経平均株価', '米ドル対円相場', 'ユーロ対円相場'
);
